
public abstract class  Pessoa {
	
	private String cpf;
	private String endereco;
	private String nome;
	private String data_nasc;
	
	
	public Pessoa(String cpf, String endereco, String nome, String data_nasc) {
		this.cpf = cpf;
		this.endereco = endereco;
		this.nome = nome;
		this.data_nasc = data_nasc;
	}
	
	
	
}
